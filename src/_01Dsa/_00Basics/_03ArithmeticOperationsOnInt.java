package _01Dsa._00Basics;

public class _03ArithmeticOperationsOnInt {
  public static void main(String[] args) {
       int x = 2, y = 5;
       System.out.println(x + y);
       System.out.println(x * y);
       System.out.println(x - y);
       System.out.println(x / y);
       double x1 = 2, y1 = 5;
       System.out.println(x1 + y1);
       System.out.println(x1 * y1);
       System.out.println(x1 - y1);
       System.out.println(x1 / y1);


  }
}
